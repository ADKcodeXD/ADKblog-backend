    public static int getNumInString(String s){
        String regEx="[^0-9]";
        Pattern p = Pattern.compile(regEx);
        Matcher m = p.matcher(s);
        String trim = m.replaceAll("").trim();
        int i = Integer.parseInt(trim);
        return i;
    }

    /**
     * 根据现在的时间 判断是几月番 并且获取指定的时间
     * @return
     */
    public static Long getTimestamp() throws ParseException {
        //根据现在的时间 判断是几月番
        Calendar calendar = Calendar.getInstance();
        int year = calendar.get(Calendar.YEAR);
        int month = calendar.get(Calendar.MONTH + 1);
        //用于判断根据什么时间来进行操作
        if (month >= 1 && month < 4) {
            month = 1;
        } else if (month >= 4 && month < 7) {
            month = 4;
        } else if (month >= 7 && month < 10) {
            month = 7;
        } else {
            month = 10;
        }
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String source = year + "-";
        if (month < 10) {
            source += "0" + month;
        } else
            source += month;
        source += "-01";
        java.util.Date date = df.parse(source);
        Calendar cal = Calendar.getInstance();
        cal.setTime(date);
        long timestamp = cal.getTimeInMillis();
        return timestamp;
    }
    public static Long getTimestamp(String formatStr) throws ParseException {
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        java.util.Date date = df.parse(formatStr);
        Calendar cal = Calendar.getInstance();
        cal.setTime(date);
        long timestamp = cal.getTimeInMillis();
        return timestamp;
    }
    public static String getTime(Long stamp) throws ParseException {
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String format = df.format(new Date(stamp));
        return format;
    }
